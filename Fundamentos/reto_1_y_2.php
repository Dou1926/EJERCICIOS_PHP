<?php
// Variables de ejemplo para la edad y la altura
$edad = 30;
$altura = 1.75;

// Suma de la edad y la altura
$suma = $edad + $altura;
echo "Suma: " . $suma . "<br>";

// Resta de la edad y la altura
$resta = $edad - $altura;
echo "Resta: " . $resta . "<br>";

// Multiplicación de la edad y la altura
$multiplicacion = $edad * $altura;
echo "Multiplicación: " . $multiplicacion . "<br>";

// División de la edad y la altura
$division = $edad / $altura;
echo "División: " . $division . "<br>";

// Potencia de la edad elevada a la altura
$potencia = pow($edad, $altura);
echo "Potencia: " . $potencia . "<br>";

// Raíz cuadrada de la edad
$raizCuadrada = sqrt($edad);
echo "Raíz Cuadrada de la Edad: " . $raizCuadrada;
?>
